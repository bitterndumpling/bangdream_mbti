def build_block(item: dict) -> str:
    lines = [
        "",
        "## 外部补充设定",
        "",
        item["setting"],
        "",
        f"从外部资料归纳，{item['name']} 的设定补充可以抓住这几点：",
        "",
    ]
    lines.extend(f"- {point}" for point in item["axes"])
    lines.extend([
        "",
        "## 外部剧情补充",
        "",
    ])
    lines.extend(f"- {point}" for point in item["story"])
    lines.extend([
        "",
        "## 参考来源",
        "",
    ])
    for label, url in BAND_SOURCES[item["group"]]:
        lines.append(f"- [{label}]({url})")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    updated = []
    missing = []

    for folder, item in SUPPLEMENTS.items():
        if folder in SKIP_FOLDERS:
            continue

        path = ROOT / folder / "portrait.md"
        if not path.exists():
            missing.append(folder)
            continue

        text = path.read_text(encoding="utf-8")
        if "## 外部补充设定" in text:
            continue

        path.write_text(text.rstrip() + "\n" + build_block(item), encoding="utf-8")
        updated.append(folder)

    print(f"updated={len(updated)}")
    for folder in updated:
        print(folder)

    if missing:
        print("missing:")
        for folder in missing:
            print(folder)


if __name__ == "__main__":
    main()
